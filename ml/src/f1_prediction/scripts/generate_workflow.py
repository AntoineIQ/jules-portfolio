"""
Regenerate ``.github/workflows/refresh-sessions.yml`` so its cron triggers
fire ~30 minutes after each F1 session in the next 7 days.

Run weekly by ``.github/workflows/scheduler.yml``. The smartness lives here
rather than in a hand-written cron — by reading the F1 schedule itself we
get session-end triggers that track the actual calendar: FP1/FP2/FP3/Q/R
on a normal weekend, plus Sprint sessions where applicable, and nothing
at all during summer break.

Usage:
    uv run python -m f1_prediction.scripts.generate_workflow
"""

from __future__ import annotations

import logging
from datetime import UTC, datetime, timedelta
from pathlib import Path

import fastf1
import pandas as pd

logger = logging.getLogger(__name__)
logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(message)s")

# Repo root: the file lives at ml/src/f1_prediction/scripts/generate_workflow.py
# so parents[4] climbs back up to the repo root.
REPO_ROOT = Path(__file__).resolve().parents[4]
WORKFLOW_PATH = REPO_ROOT / ".github" / "workflows" / "refresh-sessions.yml"

# Approximate session durations (hours) used to estimate end time.
# We deliberately over-estimate slightly so we don't fire before data is available.
SESSION_DURATION_HOURS: dict[str, float] = {
    "Practice 1": 1.5,
    "Practice 2": 1.5,
    "Practice 3": 1.5,
    "Sprint Qualifying": 1.0,
    "Sprint Shootout": 1.0,
    "Sprint": 1.25,
    "Qualifying": 1.5,
    "Race": 2.5,
}
DEFAULT_DURATION_HOURS = 1.5
POST_SESSION_BUFFER_MIN = 30  # give FastF1/API a few minutes to settle
HORIZON_DAYS = 7


def find_upcoming_triggers(
    year: int,
    now_utc: datetime,
    horizon_days: int = HORIZON_DAYS,
) -> list[tuple[str, str, datetime]]:
    """
    Return a list of (event_name, session_name, trigger_time_utc) for every
    F1 session whose end time + post-session buffer falls inside the horizon.
    """
    sched = fastf1.get_event_schedule(year, include_testing=True)
    upcoming: list[tuple[str, str, datetime]] = []
    horizon = now_utc + timedelta(days=horizon_days)

    for _, event in sched.iterrows():
        event_name = str(event["EventName"])
        for i in range(1, 6):
            sname_val = event.get(f"Session{i}")
            sdate_val = event.get(f"Session{i}Date")
            if sname_val is None or pd.isna(sname_val):
                continue
            if sdate_val is None or pd.isna(sdate_val):
                continue
            sname = str(sname_val)
            start = pd.Timestamp(sdate_val).tz_convert("UTC").to_pydatetime()
            duration = timedelta(hours=SESSION_DURATION_HOURS.get(sname, DEFAULT_DURATION_HOURS))
            trigger = start + duration + timedelta(minutes=POST_SESSION_BUFFER_MIN)
            if now_utc < trigger <= horizon:
                upcoming.append((event_name, sname, trigger))

    upcoming.sort(key=lambda t: t[2])
    return upcoming


def to_cron(trigger_utc: datetime) -> str:
    """
    Convert a UTC datetime to a GitHub Actions cron line.

    GH Actions cron has 5-minute granularity in practice; we round the minute
    down to the nearest 5. Day-of-week is wild because we pin the specific
    day-of-month instead, which is safer across week boundaries.
    """
    minute = (trigger_utc.minute // 5) * 5
    return f"{minute} {trigger_utc.hour} {trigger_utc.day} {trigger_utc.month} *"


def render_workflow(triggers: list[tuple[str, str, datetime]], year: int) -> str:
    """Render the refresh-sessions.yml file content from the triggers."""
    header = [
        "name: Refresh F1 data after session",
        "# AUTO-GENERATED by scripts/generate_workflow.py.",
        "# Do not edit by hand — the scheduler workflow rewrites this file weekly.",
        "",
        "on:",
    ]

    schedule_block: list[str] = []
    if triggers:
        schedule_block.append("  schedule:")
        seen_crons: set[str] = set()
        for ev, sn, trig in triggers:
            cron = to_cron(trig)
            if cron in seen_crons:
                continue
            seen_crons.add(cron)
            schedule_block.append(
                f"    - cron: '{cron}'  # {ev} — {sn} @ {trig.strftime('%Y-%m-%d %H:%M')}Z"
            )
    else:
        schedule_block.append("  # no upcoming sessions in the next week; schedule block empty")

    body = [
        "  workflow_dispatch:",
        "",
        "permissions:",
        "  contents: write",
        "",
        "concurrency:",
        "  group: f1-refresh",
        "  cancel-in-progress: false",
        "",
        "jobs:",
        "  refresh:",
        "    runs-on: ubuntu-latest",
        "    timeout-minutes: 60",
        "    steps:",
        "      - uses: actions/checkout@v4",
        "        with:",
        "          fetch-depth: 0",
        "      - name: Install libomp (LightGBM runtime)",
        "        run: sudo apt-get update && sudo apt-get install -y libomp-dev",
        "      - uses: actions/setup-node@v4",
        "        with:",
        "          node-version: '22'",
        "          cache: npm",
        "          cache-dependency-path: apps/web/package-lock.json",
        "      - name: Install web dependencies",
        "        working-directory: apps/web",
        "        run: npm ci",
        "      - uses: astral-sh/setup-uv@v5",
        "        with:",
        "          enable-cache: true",
        "      - name: Install project",
        "        working-directory: ml",
        "        run: uv sync --extra train --extra dev",
        "      - name: Ingest current year",
        "        working-directory: ml",
        f"        run: uv run python -m f1_prediction.ingest --year {year}",
        "      - name: Publish static payloads and serving artifacts",
        "        working-directory: ml",
        "        run: uv run python -m f1_prediction.publish",
        "      - name: Verify ML serving surface",
        "        working-directory: ml",
        "        run: uv run pytest -q tests/test_artifacts.py tests/test_api.py",
        "      - name: Verify web app",
        "        working-directory: apps/web",
        "        run: |",
        "          npm run typecheck",
        "          npm run build",
        "      - name: Commit refresh outputs (if any)",
        "        run: |",
        "          git config user.name 'github-actions[bot]'",
        "          git config user.email '41898282+github-actions[bot]@users.noreply.github.com'",
        "          git add ml/data/raw/ apps/web/public/data/f1/ ml/artifacts/serving/",
        "          if ! git diff --cached --quiet; then",
        "            git commit -m \"data: refresh F1 artifacts $(date -u +'%Y-%m-%d %H:%M UTC')\"",
        "            git push",
        "          else",
        "            echo 'No new data or artifact changes to commit'",
        "          fi",
    ]

    return "\n".join(header + schedule_block + body) + "\n"


def main() -> None:
    now = datetime.now(UTC)
    year = now.year
    triggers = find_upcoming_triggers(year, now)
    logger.info("Found %s upcoming session triggers in the next %s days", len(triggers), HORIZON_DAYS)
    for ev, sn, trig in triggers:
        logger.info("  %s — %s @ %sZ", ev, sn, trig.strftime("%Y-%m-%d %H:%M"))
    content = render_workflow(triggers, year=year)
    WORKFLOW_PATH.parent.mkdir(parents=True, exist_ok=True)
    WORKFLOW_PATH.write_text(content, encoding="utf-8")
    logger.info("Wrote %s (%s bytes)", WORKFLOW_PATH, len(content))


if __name__ == "__main__":
    main()
